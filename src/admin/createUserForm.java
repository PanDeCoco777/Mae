    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        if(ln.getText().isEmpty() || em.getText().isEmpty() || un.getText().isEmpty() || ps.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill in all required Data.");
        }else if (ps.getText().length() < 8) {
            JOptionPane.showMessageDialog(null, "Password should have at least 8 characters or Above.");
            ps.setText("");
        }else if(duplicateCheck()){    
            System.out.println("Duplicated Exist");
        }else{
            dbConnector dbc = new dbConnector();

            try{    
            String pass = passwordHash.hashPassword(ps.getText());
            
            if(dbc.insertData("INSERT INTO tbl_user (u_fname, u_lname, u_email, u_username, u_password, u_type, u_status)"
                    +" VALUES ('"+uid.getText()+"', '"+ln.getText()+"', '"+em.getText()+"', '"+un.getText()+"', '"
                    +pass+"', '"+ut.getSelectedItem()+"', '"+st.getSelectedItem()+ "')")){                                        
                JOptionPane.showMessageDialog(null, "Inserted Successfully!");
                loginForm lf = new loginForm();
                lf.setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Connection Error!");
            }
        }catch(NoSuchAlgorithmException ex){
                System.out.println(""+ex);
                
        }
        }
    }//GEN-LAST:event_AddActionPerformed

